namespace KingSurvivalGame
{
    /// <summary>
    /// Enumeration, representing types of the figures 
    /// </summary>
    public enum FigureType
    {
        /// <summary>
        /// Figure King. Can go to neighbor cells in 4 directions: down-left, down-right, up-left and up-right
        /// </summary>
        King,

        /// <summary>
        /// Figure Pawn. Can go to neighbor cells in 2 directions: down-left and down-right
        /// </summary>
        Pawn
    }
}
